import { TestBed, getTestBed } from '@angular/core/testing';
import { HttpClientTestingModule, HttpTestingController } from '@angular/common/http/testing';
import * as moment from 'moment';
import { DATE_FORMAT } from 'app/shared/constants/input.constants';
import { VocabularyChangeService } from 'app/entities/vocabulary-change/vocabulary-change.service';
import { IVocabularyChange, VocabularyChange } from 'app/shared/model/vocabulary-change.model';

describe('Service Tests', () => {
  describe('VocabularyChange Service', () => {
    let injector: TestBed;
    let service: VocabularyChangeService;
    let httpMock: HttpTestingController;
    let elemDefault: IVocabularyChange;
    let expectedResult: IVocabularyChange | IVocabularyChange[] | boolean | null;
    let currentDate: moment.Moment;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [HttpClientTestingModule]
      });
      expectedResult = null;
      injector = getTestBed();
      service = injector.get(VocabularyChangeService);
      httpMock = injector.get(HttpTestingController);
      currentDate = moment();

      elemDefault = new VocabularyChange(0, 0, 0, 'AAAAAAA', 'AAAAAAA', 0, 'AAAAAAA', currentDate);
    });

    describe('Service methods', () => {
      it('should find an element', () => {
        const returnedFromService = Object.assign(
          {
            date: currentDate.format(DATE_FORMAT)
          },
          elemDefault
        );

        service.find(123).subscribe(resp => (expectedResult = resp.body));

        const req = httpMock.expectOne({ method: 'GET' });
        req.flush(returnedFromService);
        expect(expectedResult).toMatchObject(elemDefault);
      });

      it('should create a VocabularyChange', () => {
        const returnedFromService = Object.assign(
          {
            id: 0,
            date: currentDate.format(DATE_FORMAT)
          },
          elemDefault
        );

        const expected = Object.assign(
          {
            date: currentDate
          },
          returnedFromService
        );

        service.create(new VocabularyChange()).subscribe(resp => (expectedResult = resp.body));

        const req = httpMock.expectOne({ method: 'POST' });
        req.flush(returnedFromService);
        expect(expectedResult).toMatchObject(expected);
      });

      it('should update a VocabularyChange', () => {
        const returnedFromService = Object.assign(
          {
            vocabularyId: 1,
            versionId: 1,
            changeType: 'BBBBBB',
            description: 'BBBBBB',
            userId: 1,
            userName: 'BBBBBB',
            date: currentDate.format(DATE_FORMAT)
          },
          elemDefault
        );

        const expected = Object.assign(
          {
            date: currentDate
          },
          returnedFromService
        );

        service.update(expected).subscribe(resp => (expectedResult = resp.body));

        const req = httpMock.expectOne({ method: 'PUT' });
        req.flush(returnedFromService);
        expect(expectedResult).toMatchObject(expected);
      });

      it('should return a list of VocabularyChange', () => {
        const returnedFromService = Object.assign(
          {
            vocabularyId: 1,
            versionId: 1,
            changeType: 'BBBBBB',
            description: 'BBBBBB',
            userId: 1,
            userName: 'BBBBBB',
            date: currentDate.format(DATE_FORMAT)
          },
          elemDefault
        );

        const expected = Object.assign(
          {
            date: currentDate
          },
          returnedFromService
        );

        service.query().subscribe(resp => (expectedResult = resp.body));

        const req = httpMock.expectOne({ method: 'GET' });
        req.flush([returnedFromService]);
        httpMock.verify();
        expect(expectedResult).toContainEqual(expected);
      });

      it('should delete a VocabularyChange', () => {
        service.delete(123).subscribe(resp => (expectedResult = resp.ok));

        const req = httpMock.expectOne({ method: 'DELETE' });
        req.flush({ status: 200 });
        expect(expectedResult);
      });
    });

    afterEach(() => {
      httpMock.verify();
    });
  });
});
